"use strict";
let x = 3.1;
let y = 3.1;
//CONSTANTS:
let Pi = Math.PI;
let E = Math.E;
//BASIC METHODS.
let abs = Math.abs(x);
let ceil = Math.ceil(x);
let floor = Math.floor(x);
//TRIGNOMETRIC METHOD:
let sin = Math.sin(x);
let cos = Math.cos(x);
let tan = Math.tan(x);
// EXPONENTIAL AND LOGARITMIC METHODS:
let exp = Math.exp(x);
let log = Math.log(x);
let log10 = Math.log10(x);
// POWER AND SQUARE ROOT METHOD:
let pow = Math.pow(x, y);
let sqrt = Math.sqrt(x);
// RANDOM NUMBER METHOD:
let random = Math.random() * 10;
// ADDITIONAL METHOD
let min = Math.min(21, 23, 43, 455, 656, 76767);
let max = Math.max(21, 23, 43, 455, 656, 76767);
